     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $fields)
    {
        //
       
       $fields->validate([
            "name" =>  "required",
            "email" => "required",
            "role" =>  "required",
        ]);

        $newUser = User::create([
            "name"=>$fields["name"],
            "email"=>$fields["email"],
            "role"=>$fields["role"],
            "phone"=>$fields["phone"],
            "status"=>"active",
            "password"=>Hash::make("password"),
            "address"=>$fields["address"],
        ]);

        return $newUser ? redirect(route("vendor.index"))->withSuccess("New User Added"): redirect(route("vendor.index"))->withError("Operation failed");


    }

